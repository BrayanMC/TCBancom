import UIKit

public extension UIFont {
    
    static var nunitoRegular10: UIFont {
        return UIFont(name: NunitoFont.Regular.rawValue, size: 10)!
    }
    
    static var nunitoRegular12: UIFont {
        return UIFont(name: NunitoFont.Regular.rawValue, size: 12)!
    }
    
    static var nunitoRegular14: UIFont {
        return UIFont(name: NunitoFont.Regular.rawValue, size: 14)!
    }
    
    static var nunitoMedium16: UIFont {
        return UIFont(name: NunitoFont.Regular.rawValue, size: 16)!
    }
    
    static var nunitoBold16: UIFont {
        return UIFont(name: NunitoFont.Bold.rawValue, size: 16)!
    }
}

